package DTOs;

import java.io.Serializable;

/**
 *
 * @author pabli
 */
public class HomeEspectaculoDTO implements Serializable{
    private String nombre;
    private String imagen;

    public HomeEspectaculoDTO() {
    }

    public HomeEspectaculoDTO(String nombre, String imagen) {
        this.nombre = nombre;
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public String getImagen() {
        return imagen;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
    
}
